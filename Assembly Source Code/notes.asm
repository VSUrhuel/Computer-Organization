MOV AL, 01H ;CURSOR MOVED
LEA BP, STR ;STR ADDRESS
MOV DX, 00 ;ROW AND COL
MOV BX, 0FH ;COLOR DESIGN
MOV CX, 07 ;STR LENGTH
INT 10H    


